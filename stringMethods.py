spam='Hello World'
spam=spam.upper() #upper() converts the string into all uppercase
print(spam)

spam=spam.lower() #lower() converts the string into all lowercase
print(spam)

stri='abhay shekhar yadav'
print(stri.isupper()) #isupper returns true if  all are uppercase

str1='Abhay SHEKHAR YADav'
